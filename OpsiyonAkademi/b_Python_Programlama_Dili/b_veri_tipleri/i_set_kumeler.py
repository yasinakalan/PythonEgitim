# Kumeler(Set)

# liste --> liste = [1, 2, 2, 5, 5, 6]
# dictionary --> sozluk = {key:value, key-value, ...}
# kumeler --> Listeler gibidir. Fakat aynı değerler tekrarlanmaz. Süslü parantez kullanılır. Liste gibi içi doldurulur. Küçükten büyüğe sıralama yapar.

a = [6,6,6,6,6,6,6]
print("liste: ", a)

b = {6,6,6,6,6,6}       # liste:  [6, 6, 6, 6, 6, 6, 6]
print("kume: ", b)      # kume:  {6}

yeni_kume = {2,6,1,3,4,5,2,1,1,6,3}
print("kume-2: ", yeni_kume)        # kume-2:  {1, 2, 3, 4, 5, 6}
