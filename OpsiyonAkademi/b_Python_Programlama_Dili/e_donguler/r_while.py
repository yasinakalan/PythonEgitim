# loop(dongu): Bir işlemi True olduğu sürece devam etmesini, aynı işlemleri yapmasını sağlar.
# while: Durdurulmadığı veya True olduğu sürece sonsuza kadar devam eder.

# NOT: Döngüyü kesmek için Terminal ekranında Ctrl+C ye bas.


a = 0       # İlk değer ataması yapıldı.

