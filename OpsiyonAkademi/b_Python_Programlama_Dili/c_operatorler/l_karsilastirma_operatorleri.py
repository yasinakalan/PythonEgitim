# Bool (0,1) cebirine göre işlem yapılacak. 0-False, 1-True değerdir.

# == eşit olup olmadığını sorgulamak için kullanılır.
# != eşit değil midir sorusunu sormak için kullanılır.
# <, >, <=, >= eşitsizlik sorgulamaları yapmak için kullanılır.

a = 3
b = 2

print("a, b'ye eşit midir?: ", a==b)
print("a, b'ye eşit değil midir?: ", a!=b)
print("a, b'den küçük müdür?: ", a<b)
print("a, b'den büyük müdür?: ", a>b)
print("a, b'den küçük-eşit midir?: ", a<=b)
print("a, b'den büyük-eşit midir?: ", a>=b)