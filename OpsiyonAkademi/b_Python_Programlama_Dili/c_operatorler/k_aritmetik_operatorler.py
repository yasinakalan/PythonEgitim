# +, -, *, /, **(üstel), %(bölümünden kalanı verir), //(bölümdeki tam sayıyı verir)

a, b = 11, 2  # Tek satırda 2 değişken tanımlanmıştır.

# Toplama-Çıkarma:
print("a+b=", a+b)
print("a-b=", a-b)

# Çarpma-Bölme:
print("a*b=", a*b)
print("a/b=", a/b)

# Üs alma:
print("a**b=", a**b)

# Bölümden Kalanı Bulma:
print("a nın b ye bölümünden kalan(a%'b)=", a%b)

# Bölümdeki Tam Sayı Kısmını Bulma:
print("a//b=", a//b)