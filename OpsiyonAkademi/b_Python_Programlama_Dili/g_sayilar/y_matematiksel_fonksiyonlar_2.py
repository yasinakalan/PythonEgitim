import math

# math. yazıldığında içindeki fonksiyonlar görülebilir.

a = 3.01
b = math.sin(a)
print("sinüs alma: ", b)

print("integer kısmı alma: ", math.floor(a))
print("bir sonraki integera yuvarla: ", math.ceil(a))
print("exponansiyel sayıyı bulma(e^a): ", math.exp(a))
print("faktöriyel alma: ", math.factorial(int(a)))
print("karekök alma: ", math.sqrt(a))
